print('Addition of Two Numbers')
# Step 1: Prompt or asking the user to enter the first number
num1 = float(input("Enter the first number: "))

# Step 2: Prompt or asking the user to enter the second number
num2 = float(input("Enter the second number: "))

# Step 3: Adding the two numbers and store the result
add = num1 + num2

# Step 4: Print the result
print("The sum of", num1, "and", num2, "is", add)
